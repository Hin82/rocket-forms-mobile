function getTranslationLang(code: LanguageCode): string {
  if (translations[code]) return code;
  return 'en';
}

export function useTranslation() {
  const { language } = useLanguage();
  const lang = getTranslationLang(language);

  function t(section: string, key: string, params?: Record<string, string>): string {
    const value = translations[lang]?.[section]?.[key]
      ?? translations['en']?.[section]?.[key]
      ?? key;

    if (!params) return value;

    return Object.entries(params).reduce(
      (str, [k, v]) => str.replace(`{${k}}`, v),
      value,
    );
  }

  return { t, language };
}
